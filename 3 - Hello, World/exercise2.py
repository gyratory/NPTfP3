# 2. Write a program that shows the use of all 7 math functions.
print("This is a program that will demonstrate all 7 math functions in python3.")
print("2 ** 3 =", 2 ** 3) # Exponentiation
print("8 * 2 =", 8 * 2) # Multiplication
print("4 / 2 =", 4 / 2) # Division
print("8 // 5 =", 8 // 5) # Integer Division
print("8 % 5 =", 8 % 5) # Remainder
print("10 + 19 =", 10 + 19) # Addition
print("3 - 4 =", 3 - 4) # Subtraction
